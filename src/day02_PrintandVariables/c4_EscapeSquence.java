package day02_PrintandVariables;

public class c4_EscapeSquence {

    public static void main(String[] args){
        // \t it is going to leave paragraph space on the console

        System.out.println("This is the line and if I want to put paragraph space\tasdfniuoeqg");

        // \n it is going to move on new line on the console

        System.out.println("This is a code where I go to next \nline");

        // \" prints double quote " on the console

        System.out.println("this is \"Anil\"");

        // \' prints single quote ' on the console

        System.out.println("He said \'I want to go home\'");

        //  \t:paragraph space
        //  \n: new line
        //  \": prints double quote " on the console
        //  \\: prints \ on the console
        //  \': prints single quote
        //  ' : prints single quote




    }
}
